"""B7 — Held-out partition reproducibility check.

Re-runs `tools/test_split.py` from a clean state for each task in
`docs/significance_tasks.yaml` and verifies that the seed-7 holdout
indices used to produce committed engine results match what the split
function produces today. Pre-empts the "did the split actually freeze?"
critique on §6.10.

Run from clean checkout:
    uv run tools/repro_check.py --output docs/reproducibility-checks.md
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO / "tools"))
from test_split import split_test  # noqa: E402

import yaml  # noqa: E402


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--output", type=Path,
                   default=REPO / "docs" / "reproducibility-checks.md",
                   help="Markdown output path.")
    a = p.parse_args()

    spec = yaml.safe_load((REPO / "docs/significance_tasks.yaml").read_text())

    rows = []
    fail = 0
    for s in spec["tasks"]:
        name = s["task_name"]
        seed = s.get("split_seed", 7)
        engine_path = REPO / s["engine_path"]
        if not engine_path.exists():
            rows.append((name, seed, "—", "—", "SKIP", "engine result missing"))
            continue

        dev, holdout = split_test(name, seed=seed, dev_fraction=0.5)
        h = set(holdout)
        d = json.loads(engine_path.read_text())
        committed_indices = sorted(r["index"] for r in d.get("per_case", [])
                                    if "index" in r)
        committed_set = set(committed_indices)

        if s.get("filter_to_holdout"):
            missing = h - committed_set
            extra = committed_set - h - set(dev)  # extras outside both halves
            if missing:
                rows.append((name, seed, len(committed_indices), len(holdout),
                             "FAIL", f"holdout indices missing from full-test: {sorted(missing)[:3]}"))
                fail += 1
            elif extra:
                rows.append((name, seed, len(committed_indices), len(holdout),
                             "FAIL", f"committed indices outside dev∪holdout: {sorted(extra)[:3]}"))
                fail += 1
            else:
                rows.append((name, seed, len(committed_indices), len(holdout),
                             "OK", f"all {len(holdout)} holdout indices present in full-test result"))
        else:
            if committed_set == h:
                rows.append((name, seed, len(committed_indices), len(holdout),
                             "OK", f"committed indices = holdout exactly (n={len(holdout)})"))
            else:
                rows.append((name, seed, len(committed_indices), len(holdout),
                             "FAIL", f"set mismatch: committed n={len(committed_indices)}, holdout n={len(holdout)}"))
                fail += 1

    md = []
    md.append("# Reproducibility check — held-out partition")
    md.append("")
    md.append(f"Generated by `tools/repro_check.py`. Verifies that the "
              f"seed-7 50-50 dev/holdout partition produced by "
              f"`tools/test_split.py` today matches the index set used "
              f"in committed engine results. Pre-empts the §6.10 "
              f"\"did the split actually freeze?\" critique.")
    md.append("")
    md.append(f"**Result: {len(rows) - fail}/{len(rows)} OK** "
              f"({fail} failures, "
              f"{sum(1 for r in rows if r[4] == 'SKIP')} skipped).")
    md.append("")
    md.append("| Task | Seed | Committed n | Holdout n | Status | Detail |")
    md.append("|---|---:|---:|---:|---|---|")
    for name, seed, cn, hn, status, detail in rows:
        md.append(f"| `{name}` | {seed} | {cn} | {hn} | "
                  f"{'✅' if status == 'OK' else '❌' if status == 'FAIL' else '⏭'} {status} | {detail} |")
    md.append("")
    md.append("## Interpretation")
    md.append("")
    md.append("All `OK` rows confirm that re-running `tools/test_split.py "
              "--task <name> --seed 7 --dev_fraction 0.5` from a clean "
              "checkout produces the same holdout indices as the committed "
              "engine results were filtered against (or evaluated on, for "
              "tasks that ran directly on the holdout split).")
    md.append("")
    md.append("This is the empirical confirmation referenced from §6.10.6 "
              "of the v3.8 paper for the held-out methodology claim.")
    md.append("")
    md.append(f"Re-run with: `uv run tools/repro_check.py`")

    out_text = "\n".join(md)
    a.output.parent.mkdir(parents=True, exist_ok=True)
    a.output.write_text(out_text)
    print(out_text)
    print(f"\nWrote {a.output}", file=sys.stderr)
    return 0 if fail == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
